export function normalizeSearchText(value) {
  return String(value || '').toLowerCase().replace(/\s+/g, '').replace(/[座星]/g, '');
}

export function searchConstellations(query, limit = 10) {
  const needle = normalizeSearchText(query);
  return CONSTELLATION_TARGETS.map(item => {
    const values = [item.nameZh, item.nameEn, ...(item.aliases || [])].map(normalizeSearchText);
    let score = 99;
    for (const value of values) {
      if (!needle) score = Math.min(score, 10);
      else if (value === needle) score = Math.min(score, 0);
      else if (value.startsWith(needle)) score = Math.min(score, 1);
      else if (value.includes(needle)) score = Math.min(score, 2);
    }
    return { item, score };
  }).filter(result => result.score < 99)
    .sort((a, b) => a.score - b.score || a.item.nameZh.localeCompare(b.item.nameZh, 'zh-Hant'))
    .slice(0, limit).map(result => result.item);
}
